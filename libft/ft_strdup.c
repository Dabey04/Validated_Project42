#include "libft.h"

char	*ft_strdup(char const *src)
{
	int		i;
	char	*dest;

	i = 0;
	if (!(dest = (char *)malloc((ft_strlen(src) + 1) * sizeof(char))))
		return (0);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

#include "libft.h"

long	ft_atoi(const char *str)
{
	int		i;
	int		nb;
	long	res;

	i = 0;
	nb = 1;
	res = 0;
	if (str[i] == '-')
	{
		nb = -nb;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	return (res * nb);
}
